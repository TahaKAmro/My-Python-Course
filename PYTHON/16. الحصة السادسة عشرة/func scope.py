x = 0
# نعرف متغير عالمي 

def change_x():
    x = 1
    # تعريف متغير محلي
    print(f'this is x from local : {x}')
# هذا الأمر سيقوم بطباعة قيمة المتغير المحلي

print(f'this is x from global : {x}') # طباعة المتغير العالمي
change_x() # استدعاء الأمر

