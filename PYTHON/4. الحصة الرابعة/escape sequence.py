# \ <-- backslash is a special character in the language

print('hello wor\bld') # we use it with (b) to delet a letter

print('hello \
world \
im') # we use it alone to start a new line without printing it

print('hello \n world \n im') # we use it with (n) to start a new line when printing

print('hello \'world\'') # we use it to tell the code that the ('') are strings to print

print("hello \"world\"") # we use it to tell the code that the ("") are strings to print